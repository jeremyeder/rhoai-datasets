"""src/datasets/recommender/ai_detection.py -- LLM-as-judge AI-content detection."""

from __future__ import annotations

import json
import os

import httpx

from datasets.metadata.schema import AIDetectionResult

JUDGE_PROMPT = """You are an AI-content detector. Analyze the following text and determine
how likely it is to have been generated by an AI language model.

Consider these signals:
- Unnaturally consistent formatting and structure
- Lack of typos, colloquialisms, or informal language
- Overly comprehensive or exhaustive coverage
- Generic phrasing patterns common in LLM output
- Perfect grammar throughout
- Boilerplate-style explanations

Respond with a JSON object:
{{"score": <float 0.0-1.0>, "reasoning": "<brief explanation>"}}

Where 0.0 = definitely human-written, 1.0 = definitely AI-generated.

Text to analyze:
---
{text}
---"""


def parse_judge_response(response: str) -> float:
    try:
        data = json.loads(response)
        score = float(data["score"])
        return max(0.0, min(1.0, score))
    except (json.JSONDecodeError, KeyError, TypeError, ValueError):
        if any(
            word in response.lower()
            for word in ["ai generated", "ai-generated", "likely ai"]
        ):
            return 0.7
        if any(word in response.lower() for word in ["human", "natural", "organic"]):
            return 0.3
        return 0.5


def _call_judge(text: str, model: str) -> str:
    api_key = os.environ.get("ANTHROPIC_API_KEY", "")
    if not api_key:
        raise RuntimeError(
            "ANTHROPIC_API_KEY environment variable required for AI detection"
        )

    response = httpx.post(
        "https://api.anthropic.com/v1/messages",
        headers={
            "x-api-key": api_key,
            "anthropic-version": "2023-06-01",
            "content-type": "application/json",
        },
        json={
            "model": model,
            "max_tokens": 256,
            "messages": [{"role": "user", "content": JUDGE_PROMPT.format(text=text)}],
        },
        timeout=30.0,
    )
    response.raise_for_status()
    data = response.json()
    return data["content"][0]["text"]


def detect_ai_content(
    artifacts: dict[str, str],
    model: str = "claude-sonnet-4-6",
) -> AIDetectionResult:
    per_artifact: dict[str, float] = {}

    for name, content in artifacts.items():
        if not content.strip():
            per_artifact[name] = 0.0
            continue
        truncated = content[:4000]
        response = _call_judge(truncated, model)
        per_artifact[name] = parse_judge_response(response)

    if per_artifact:
        overall = sum(per_artifact.values()) / len(per_artifact)
    else:
        overall = 0.0

    return AIDetectionResult(
        overall_score=round(overall, 3),
        per_artifact_scores=per_artifact,
        method="llm-as-judge",
        model=model,
    )
